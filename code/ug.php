<h2>Academic Eligibility</h2>

<p>
The Candidate has passed the II-Year Pre-University or XII Std. or equivalent exams with Physics and Mathematics as compulsory subjects, along with one of the following subjects: Chemistry, Bio-Technology, Computer Science, Biology, Electronics & has secured not less than 45% of the aggregate marks in optional subjects with English as one of the languages for admission to Engineering and Technology courses.
	
</p>

<p>
A Candidate shall be eligible for admission to any of the full time degree courses is the holder of a diploma in appropriate branch of engineering awarded by the Board of Technical Education in Karnataka or equivalent qualification and has secured not less than 45% of marks in the final year Diploma examinations. Provided that in respect of Architecture the Candidate must have secured not less than 50% of the marks in the final year Diploma Examination and appeared for Aptitude test (NATA).
	
</p>

