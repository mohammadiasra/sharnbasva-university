       <h2>
       	
       Genesis of our college
       </h2>

<p align="justify">
	
Faculty of Engineering and Technology Exclusively for Women was started under Sharnbasva University in the year 2017 in found memory of Matoshree Godutai to pay deepest respect by way of giving higher education of par excellence to Women, and empower them to live indepently with enlightenment and vocation to pursue. Matoshree Godutai was president of Akhila Bharat Veerashaiva Mahasabha (Women Wing). Matoshree Godutai addressed as "AWWAWRE".
</p>

<p align="justify">
	
With the vision and foresight of our President Parama Poojya Dr. Sharanabaswappa Appa, Mahadasoha Peetadhipati, Sharanbasaveshwar Samsthan, Gulbarga, who has 40 years of experience in the field of Education, our management is determined to serve to the cause of the growing fields of and proficient engineering graduates in this backward region of Karnataka state, established the Women's Engineering College from the academic year 2011-2012 with an ambition to provide the world class Engineering Eduction for Women folk in this backward part of Karnataka state namely "Godutai Engineering College for Women". This is the only one Women's Engineering College in the entire Northen backward part of Karnataka state. It has resolution only for Women to move ahead of the revolution that is taking place in the areas like Computer Science, Electronics & Communication, Electrical & Electronics, and Information Science.
In the memory of Matoshree Godutai Doddappa Appa a number of colleges have been started for emancipation, fostering and nurturing them, they are
</p>

<p>Godutai Doddappa Appa Pre University College for Women (1971)</p>
<p>Godutai Doddappa Appa Arts and Commerce College for Women (1973)</p>
<p>Godutai College of Education for Women (2005)</p>
<p>Godutai Engineering College for Women (2011)</p>
<p>Godutai Doddappa Appa BCA College for Women (2001)</p>
