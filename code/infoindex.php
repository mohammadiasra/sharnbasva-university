<p align="justify">
The Department was started in the year 2011. The present intake of the undergraduate program is 60. The course has become popular among students due to the achievements and rigor. The department has well equipped classrooms and computer laboratories with high-end systems. The department affiliated to Visvesvaraya Technological University (VTU).
</p>
     

<h2>Mission</h2>
     
<p align="justify">
To provide education of excellent quality to facilitate all learners to develop their full potential as individuals by imparting a clear knowledge of the fundamentals and applied aspects of Information Science and Engineering to make them acceptable to Industries, higher Institutions of learning and society.
     
</p>

<h2>Vision</h2>
<p align="justify">
     
Dovetailing the academic pursuits with changing needs of industry.
Research, development and higher studies in information science and technology.
Attracting and Fostering talent by providing diverse, stimulating and intellectual environment for faculty and students.
</p>

