<h2>Academic Eligibility (M.Tech)</h2>
	
<p>
1. Admission to the Master of Technology Course shall be open to all the candidates who have passed B.E. / B. Tech. Examinations (as per the eligibility criteria specified from time to time) of VTU or any other recognized University / Institution.
</p>

<p>
2. The decision of the Equivalence committee shall be final in establishing the eligibility of candidates for a particular course. For the foreign degrees Equivalence certificate from the Association of Indian Universities is a must.
</p>

<p>
3. Admission to M. Tech. Course shall be open to the candidates who have passed the prescribed qualifying examination with not less than 50% of the marks in the aggregate of all the years of the degree examination. However, in the case of candidates belonging to SC/ST and Category-I, the aggregate percentage of marks in the qualifying examinations shall not be less than 45%.
</p>

<h2>Selection Procedure (M.Tech)</h2>
	
<p>1. There shall be entrance examination for PG Programs from the Karnataka Examination Authority and candidates qualified for the admission through the Entrance examination or qualified for admission under GATE and issued an admission order from KEA are eligible for the admission to M. Tech. Program or through the entrance examination conducted by the University. Government Quota: 80% seats are through centralized counseling & as per merit in PGCET conducted by Govt. of Karnataka or through GATE. Management Quota: 20% seats are filled by the college management.
</p>
	


<h2>Academic Eligibility (MBA)</h2>

<p>Graduates of Commerce, Arts, Science, Business Management, Law, Agriculture and Engineering disciplines who have obtained minimum aggregate of 50 percent of marks are eligible to seek admission to MBA Programme. Interested students are required to look out for advertisements in leading newspapers for PGCET / K-MAT Entrance Test announcements. Any one entrance test of PGCET / K-MAT / CAT / MAT is a must for admission. 50% of the seats will be allocated based on the Karnataka State PGCET scores through Visvesvaraya Technological University (VTU) counseling. The remaining 50% of the seats coming under the management quota will be allocated on the basis of the ranking in K-MAT / CAT / MAT / PGCET. The undergraduate aggregate score will also be considered for merit.
</p>
	


<h2>Selection Procedure (MBA)</h2>

<h3>1. Management Quota Seats</h3>
	
<p>The eligible students having COMED-K, UGET, PGCET, CAMAT rank can apply with all relevant documents along with a DD of Rs.500/- in favor of Principal Godutai Engineering College for Women, Gulbarga.
</p>
	

<p>For vacant seats other eligible student switch CET Govt. of Kartnataka rank/AIEEE/PGCET/CAMAT equivalent can be considered.
</p>
	

<h3>2. Government Quota Seats</h3>
	
<p>CET-KEA, Govt. of Karnataka, PGCET, CAMAT, Centralized Counseling.</p>

